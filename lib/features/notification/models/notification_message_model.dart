import 'dart:convert';

import 'package:flutter/material.dart';

class NotificationMessageModel {
  final String title;
  final String message;
  final String time;
  final IconData? icon;
  final bool isRead;

  NotificationMessageModel({
    required this.title,
    required this.message,
    required this.time,
    this.icon,
    this.isRead = false,
  });

  NotificationMessageModel copyWith({
    String? title,
    String? message,
    String? time,
    IconData? icon,
    bool? isRead,
  }) {
    return NotificationMessageModel(
      title: title ?? this.title,
      message: message ?? this.message,
      time: time ?? this.time,
      icon: icon ?? this.icon,
      isRead: isRead ?? this.isRead,
    );
  }

  Map<String, dynamic> toMap() {
    return <String, dynamic>{
      'title': title,
      'message': message,
      'time': time,
      'icon': icon?.codePoint,
      'isRead': isRead,
    };
  }

  factory NotificationMessageModel.fromMap(Map<String, dynamic> map) {
    return NotificationMessageModel(
      title: map['title'] as String,
      message: map['message'] as String,
      time: map['time'] as String,
      icon: map['icon'] != null
          ? IconData(map['icon'] as int, fontFamily: 'MaterialIcons')
          : null,
      isRead: map['isRead'] as bool,
    );
  }

  String toJson() => json.encode(toMap());

  factory NotificationMessageModel.fromJson(String source) =>
      NotificationMessageModel.fromMap(
          json.decode(source) as Map<String, dynamic>);
}

